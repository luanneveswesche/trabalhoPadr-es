package trabalhoPadrao;

// inicio do padrao proxi
public class Colaborador {

	private String nome;
    private String cidade;
	
    
    
    public Colaborador(String nome, String cidade) {
		super();
		this.nome = nome;
		this.cidade = cidade;
	}

	public String getNome() {
		return nome;
	}
	public String getCidade() {
		return cidade;
	}

 
}
